int	ft_isdigit(char	*s)
{
	int	i;

	i = 0;
	if (s[i])
	{
		while (s[i])
		{
			if (s[i] <= 57 && s[i] >= 48)
				return (1);
			i++;
		}
		return (0);
	}
	return (1);
}
